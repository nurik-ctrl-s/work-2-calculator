let num1 = 1;
let num2 = 5;
let opperator = "+";
if (opperator == "+") {
  console.log("sum :", num1 + num2);
} else if (opperator == "-") {
  console.log("sum :", num1 + num2);
} else if (opperator == "*") {
  console.log("sum :", num1 * num2);
} else if (opperator == "/") {
  console.log("sum :", num1 / num2);
} else {
  console.log("Invalid Input");
}
